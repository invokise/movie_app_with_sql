class MovieModel {
  final int filmCode;
  final String name;
  final String duration;
  final int filmStudioCode;
  final String poster;
  final String description;

  MovieModel({
    required this.filmCode,
    required this.duration,
    required this.filmStudioCode,
    required this.name,
    required this.poster,
    required this.description,
  });
}
